package cn.string.day16;

public class ForDemo {
    public static void main(String[] args) {
        int[] arr={1,2,3,4,5};
        for (int x:arr){
            System.out.println(x);
        }

        String[] strArray={"余一","沈宁"};
        for (String s:strArray){
            System.out.println(s);
        }
    }
}
